pub type Character = [[bool; 8]; 16];

const fn row(bits: u8) -> [bool; 8] {
    [
        bits & 0b1000_0000 != 0,
        bits & 0b0100_0000 != 0,
        bits & 0b0010_0000 != 0,
        bits & 0b0001_0000 != 0,
        bits & 0b0000_1000 != 0,
        bits & 0b0000_0100 != 0,
        bits & 0b0000_0010 != 0,
        bits & 0b0000_0001 != 0,
    ]
}

